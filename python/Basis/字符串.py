#字符串是 Python 中最常用的数据类型。我们可以使用引号( ' 或 " )来创建字符串。

#Python 不支持单字符类型，单字符在 Python 中也是作为一个字符串使用


# !/usr/bin/python3

var1 = 'Hello World!'
var2 = "Runoob"

print("var1[0]: ", var1[0])
print("var2[1:5]: ", var2[3:5])


# 字符串更新
# !/usr/bin/python3

var1 = 'Hello World!'

print("已更新字符串 : ", var1[:6] + 'Runoob!')